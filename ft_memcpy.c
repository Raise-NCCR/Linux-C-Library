#include <unistd.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t		i;

	i = 0;
	while (i < n)
	{
		dest = (void *)(src + i);
		i++;
		dest++;
	}
	return (dest);
}
